/**
 * 7. Write a program to print 1 to 10 using the do-while loop statement.
 */
package com.loops;

public class Loops_07 {
    public static void main(String[] args) {

        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        }
        while (i <= 10);
    }
}

